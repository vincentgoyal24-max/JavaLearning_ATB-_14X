package ex_003_Operators;

public class Lab_047_All_Operators_Revision {
  public static void main(String[] args) {
    //Arithmetic Operator
    int a = 10;
    int b = 20;
    System.out.println(a+b);
    System.out.println(a-b);
    System.out.println(a*b);
    System.out.println(a/b);
    System.out.println(a%b);

    //Compound Assignment Operator > check again.
    int c = 10;
    c += 10; // c = c + 10;
    c -= 10; // c = c - 10;
    c *= 10; // c = c * 10;
    c /= 10; // c = c / 10;
    c %= 10; // c = c % 10;

    System.out.println(c);

  }}
