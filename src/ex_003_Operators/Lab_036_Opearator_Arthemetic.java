package ex_003_Operators;

public class Lab_036_Opearator_Arthemetic {
  public static void main(String[] args) {

    // Arithmetic Operators
        //        + (Addition)
        //       - (Subtraction)
        //       * (Multiplication)
        //       / (Division)
        //       % (Modulus) | Modulus - Remainder
        int a = 20;
        int b = 3;
        float c = 3.0f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        System.out.println(a/c);
        System.out.println("a+b"); // a+b will be printed as it is because it is a string.
  }
}
