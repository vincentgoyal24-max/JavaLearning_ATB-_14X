package ex_001_Java_Basics;

public class Lab_006_MultiLineComments {
  public static void main(String[] args) {
    /*
     * name - vincent
     * age - 30
     * city - Barnala
     *
     */
    System.out.println("name - vincent");
    System.out.println("age - 30");
    System.out.println("city - Barnala");
    // Shortcut for adding multi comment line is "Command+shift+/"

    /*
    We can have multiple multi and single line comments in main methods.
     */
  }
}
