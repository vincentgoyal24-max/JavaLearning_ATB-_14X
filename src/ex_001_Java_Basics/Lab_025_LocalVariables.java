package ex_001_Java_Basics;

public class Lab_025_LocalVariables {
  public static void main(String[] args) {

    int a = 10;
    boolean b = true;
    float f = 10.5f;
    double d = 10.5;
    char c = 'c';
    short s = 10;
    long l = 10;
    byte by = 10;

    System.out.println("All these are local variables, which comes under same block {}.");
  }
}
